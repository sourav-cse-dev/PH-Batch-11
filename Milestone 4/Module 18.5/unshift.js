//New element can be added to an array using push() method from first index
const numbers = [12, 87, 98, 45];
console.log(numbers);

// we can add a single value in a array using unshift from first index
numbers.unshift(44);
console.log(numbers);

// we can add multiple values in a array using unshift from first index
numbers.unshift(98, 11, 9);
console.log(numbers);
