// Data types in JS

//String
var name = "Sourav Chowdhury";
var address = "Mirpur";
var country = "Bangladesh";
console.log(name, address, country);

//Number
var price = 2000;
console.log(price);

//Boolean
var isRich = true;
var isPassed = false;
console.log(isRich, isPassed);

// We use typeof to know the data type of a variable
console.log(typeof name);
console.log(typeof price);
console.log(isRich);
