// while loop
// The while loop loops through a block of code as long as a specified condition is true.

const number = 7;
if (number < 10) {
  console.log("Number is less than 10");
}
console.log("End of the program");

// while loop
let count = 1; //initialization of the variable, here count loop variable
while (count < 10) {
  //condition
  console.log(count);
  // count = count + 1;
  // count += 1;
  count++; //increment, change of loop variable
}

let num = 0;
while (num < 5) {
  console.log("Looping", num);
  num++;
}
