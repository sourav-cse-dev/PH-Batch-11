// for loop

let num = 0; //loop variable
while (num <= 5) {
  //loop condition
  console.log(num); //loop body
  num++; //change of loop variable
}

// for loop syntax

// for (first; second; third) {
//   first: initialization , loop variable declaration
//   second: condition , loop condition
//   third: increment/decrement
//   loop body;
// }

for (let num = 0; num <= 5; num++) {
  console.log(num);
}
