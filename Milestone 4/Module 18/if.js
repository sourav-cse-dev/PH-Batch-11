//if-else syntax

/**
 * if(condition){
  execute code if above condition is true
  }
 **/

if (5 < 10) {
  console.log("I have a  smaller value");
}

var biriyaniPrice = 250;
if (biriyaniPrice < 300) {
  console.log("Give me biriyani");
}

/**
 * if(condition){
 *  execute code if above condition is true
 * }
 * else{
 *  execute code if above condition is not true
 * }
 */

var weight = 40;
if (weight < 20) {
  console.log("I will carry it by myself");
} else {
  console.log("I will rent a rickshaw");
}
