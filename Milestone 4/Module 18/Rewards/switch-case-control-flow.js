// switch-case statement

// website theme , fixed condition

var theme = "dark";

switch (theme) {
  case "light":
    console.log("Light theme applied");
    break;
  case "dark":
    console.log("Dark theme applied");
    break;
  case "blue":
    console.log("Blue theme applied");
    break;
  default:
    console.log("System theme applied");
}

//another example of switch-case
let day = "Monday";

switch (day) {
  case "Monday":
    console.log("Start of the workweek!");
    break;
  case "Friday":
    console.log("Weekend is near!");
    break;
  case "Sunday":
    console.log("Relax, it's weekend!");
    break;
  default:
    console.log("Just another day!");
}
